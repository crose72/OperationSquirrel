/********************************************************************************
 * Function: low_pass_filter
 * Description: Return the filtered value of an input x with filter coeff alpha.
 ********************************************************************************/
float low_pass_filter(float x, float x_prev, float alpha) 
{
    return alpha * x + ((float)1.0 - alpha) * x_prev;
}